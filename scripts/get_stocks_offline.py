def load_stock_list_from_file(filename='stock_codes.csv'):
    """从文件加载股票列表"""
    if os.path.exists(filename):
        df = pd.read_csv(filename, encoding='utf-8-sig')
        print(f"✓ 从文件加载 {len(df)} 只股票")
        return df
    return None


def save_stock_list(stock_list, filename='stock_codes.csv'):
    """保存股票列表到文件"""
    stock_list.to_csv(filename, index=False, encoding='utf-8-sig')
    print(f"✓ 已保存到: {filename}")


def create_default_stock_list():
    """创建默认股票列表"""
    df = pd.DataFrame(COMMON_STOCKS)
    print(f"✓ 创建默认股票列表，共 {len(df)} 只股票")
    return df


def add_custom_codes(stock_list):
    """添加自定义股票代码"""
    print("\n添加自定义股票代码")
    print("输入格式: 代码,名称 (例如: 600519,贵州茅台)")
    print("输入空行结束")
    print()

    new_codes = []

    while True:
        user_input = input("请输入: ").strip()

        if not user_input:
            break

        if ',' in user_input:
            parts = user_input.split(',')
            if len(parts) >= 2:
                code = parts[0].strip()
                name = parts[1].strip()
                if len(code) == 6 and code.isdigit():
                    new_codes.append({'code': code, 'name': name})
                    print(f"  ✓ 已添加: {name}({code})")
                else:
                    print(f"  ✗ 无效的股票代码: {code}")
        else:
            print("  ✗ 格式错误，请使用: 代码,名称")

    if new_codes:
        new_df = pd.DataFrame(new_codes)
        stock_list = pd.concat([stock_list, new_df], ignore_index=True)

        # 去重
        stock_list = stock_list.drop_duplicates(subset=['code'], keep='last')
        print(f"\n✓ 成功添加 {len(new_codes)} 只股票")
        print(f"  当前总数: {len(stock_list)} 只")

    return stock_list


def main():
    """主函数"""
    print("=" * 60)
    print("A股股票代码管理（离线模式）")
    print("=" * 60)
    print(f"时间: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}")
    print()
    print("说明: 由于网络API连接被拒绝，使用本地股票代码列表")
    print()

    # 尝试从文件加载
    stock_list = load_stock_list_from_file()

    if stock_list is None:
        print("未找到本地股票文件，使用默认列表...")
        stock_list = create_default_stock_list()
        save_stock_list(stock_list)

    # 显示当前列表
    print("\n当前股票列表:")
    print(f"  总数: {len(stock_list)} 只")
    print(f"\n前20只股票:")
    print(stock_list.head(20).to_string())

    # 询问是否添加自定义代码
    print()
    choice = input("是否添加自定义股票代码? (y/n): ").strip().lower()

    if choice == 'y':
        stock_list = add_custom_codes(stock_list)
        save_stock_list(stock_list)

    # 询问是否导出qstock格式
    print()
    choice = input("是否导出为qstock可用格式? (y/n): ").strip().lower()

    if choice == 'y':
        df_qstock = stock_list.copy()
        df_qstock['代码'] = df_qstock['code']
        df_qstock['名称'] = df_qstock['name']

        timestamp = datetime.now().strftime('%Y%m%d_%H%M%S')
        filename = f"stock_list_qstock_{timestamp}.csv"
        df_qstock.to_csv(filename, index=False, encoding='utf-8-sig')
        print(f"✓ 已导出到: {filename}")

    print("\n" + "=" * 60)
    print("完成！")
    print("=" * 60)
    print("\n提示:")
    print("1. 股票代码已保存在: stock_codes.csv")
    print("2. 可以使用 qstock.web_data() 获取单个股票的历史数据")
    print("3. 示例: from qstock.data.trade import web_data")
    print("         data = web_data('600519', start='20240101', end='20241231', freq='d', fqt=1)")

    return stock_list


if __name__ == '__main__':
    main()
